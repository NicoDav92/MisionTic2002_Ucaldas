package controlador;

public class Bicicleta {
    // Atributos
    private String id;
    private String modelo;
    
    // Constructor
    public Bicicleta(String id, String modelo) {
        this.id = id;
        this.modelo = modelo;
    }
    
    // get

    public String getId() {
        return id;
    }

    public String getModelo() {
        return modelo;
    }
    
    
}
